import Vue from 'vue'
import Router from 'vue-router'

// in development-env not use lazy-loading, because lazy-loading too many pages will cause webpack hot update too slow. so only in production use lazy-loading;
// detail: https://panjiachen.github.io/vue-element-admin-site/#/lazy-loading

Vue.use(Router)

/* Layout */
import Layout from '../views/layout/Layout'
// import BaseRouter from '../views/layout/baseRouter'

/* Router Modules */
import contractRouterMap from './modules/contract'
import profileRouterMap from './modules/profile'
import MISRouterMap from './modules/MIS'
import workorderRouterMap from './modules/workorder'
import partRouter from './modules/part'
import energyRouterMap from './modules/energy'
import equipmentRouterMap from './modules/equipment'
import SLARouterMap from './modules/SLA'
/**
* hidden: true                   if `hidden:true` will not show in the sidebar(default is false)
* alwaysShow: true               if set true, will always show the root menu, whatever its child routes length
*                                if not set alwaysShow, only more than one route under the children
*                                it will becomes contract mode, otherwise not show the root menu
* redirect: noredirect           if `redirect:noredirect` will no redirect in the breadcrumb
* name:'router-name'             the name is used by <keep-alive> (must set!!!)
* meta : {
    title: 'title'               the name show in subMenu and breadcrumb (recommend set)
    icon: 'svg-name'             the icon show in the sidebar
    breadcrumb: false            if false, the item will hidden in breadcrumb(default is true)
  }
**/
export const constantRouterMap = [
  { path: '/login', component: () => import('@/views/login/index'), hidden: true },
  { path: '/404', component: () => import('@/views/404'), hidden: true },
  { path: '/setCookie', component: () => import('@/views/test'), hidden: true },
  {
    path: '/',
    component: Layout,
    redirect: '/dashboard',
    name: 'Dashboard',
    hidden: true,
    children: [{
      path: 'dashboard',
      component: () => import('@/views/dashboard/index')
    }]
  }
]

export const asyncRouterMap = [
  contractRouterMap,
  profileRouterMap,
  MISRouterMap,
  workorderRouterMap,
  partRouter,
  energyRouterMap,
  equipmentRouterMap,
  SLARouterMap,
  // nestedRouter,
  { path: '*', redirect: '/404', hidden: true }
]

const createRouter = () => new Router({
  // mode: 'history', // require service support
  scrollBehavior: () => ({ y: 0 }),
  routes: constantRouterMap
})

const router = createRouter()

export function resetRouter() {
  // console.log('reset')
  const newRouter = createRouter()
  router.matcher = newRouter.matcher // reset router
}

export default router
